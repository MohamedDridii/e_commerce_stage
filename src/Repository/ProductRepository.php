    public function countTotalStock(Product $product){
        return (int) $this->createQueryBuilder('p')
                ->select('Sum(s.quantity) as totalStock')
                ->join('p.stocks','s')
                ->where('p = :product')
                ->setParameter('product',$product)
                ->getQuery()
                ->getSingleScalarResult();
    }
